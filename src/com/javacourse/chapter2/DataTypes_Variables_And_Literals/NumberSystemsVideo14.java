package com.javacourse.chapter2.DataTypes_Variables_And_Literals;

public class NumberSystemsVideo14 {
    public static void main(String[] args){
        byte /*we could have any datatype*/ b1 = 10; //decimal
        byte /*we could have any datatype*/ b2 = 0b1010; //binary
        byte /*we could have any datatype*/ b3 = 012; //octal
        byte /*we could have any datatype*/ b4 = 0xA; //hexadecimal

        System.out.println(b1);
        System.out.println(b2);
        System.out.println(b3);
        System.out.println(b4);
    }
}
